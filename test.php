        $("#cidade").change(function() {
            $("#bairro").empty();
            $.ajax({
                url: "//www.ibiriteimobiliaria.com.br/adm_gerencia/estrutura/imoveis/ajaxBairros.php",
                data: {
                    'url': $("#cidade").val()
                },
                contentType: "application/json; charset=utf-8",
